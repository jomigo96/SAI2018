function p = compute_pressure(v, T)
    b1=0.002387894977169-0.000001558904110*T;
    b2=1e-5*(0.197311986301370-0.000160342465753*T);
    p0=0.422708187201682;
    p1=0.015128408366896;
    q0=86.145108796058921;
    b0 = (p1*T+p0)/(q0+T);
    p = (-b1+sqrt(b1^2-4*b2*(b0-v)))/(2*b2);
end

